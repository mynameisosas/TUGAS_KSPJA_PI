						<h2>Jaminan</h2>
						<p>Jaminan Pinjaman dapat berupa rumah/apartemen/riiko/rukan/tanah kosong yang sudah memiliki bukti Sertifikat SHM atau SHGE dan kendaraan roda dua atau roda empat yang dilengkapi dengan BPKB dan surat-surat yang lengkap</p>
						<h2>Ketentuan Jaminan</h2>
						<p>KENDARAAN</p>
						<ul>
						<li>Kendaraan roda dua (Motor) maksimal usia kendaraan tahun 2005 keatas di hitung dari sejak tahun pembuatan sampai dengan akhir masa pinjaman maksimal besar pinjaman 70% dari nilai likuidasi</li>
						<li>Kendaraan roda empat (Mobil) maksimal usia kendaraan tahun 1990 di hitung dari sejak tahun pembuatan sampai dengan akhir masa pinjaman maksimal besar pinjaman 70% dari nilai likuidasi</li>
						<li>Kendaraan bermotor yang dapat dijadikan jaminan adalah kendaraan bermotor yang terdaftar dalam Daftar Taksasi yang dikeluarkan oleh Direktorat Pinjaman</li>
						<li>Keadaan kendaraan layak pakai dan layak jalan</li>
						<li>Mempunyai nilai ekonomis, artinya dapat dinilai dengan uang dan dapat dijadikan uang</li>
						<li>Mudah dijualbelikan dan sudah dilengkapi dengan Surat Kuasa Jual</li>
						<li>Dapat dipindahtangankan kepemilikanya dari pemilik semula kepihak lain</li>
						<li>Bersedia didaftarkan untuk dilakukan pemblokiran di Kantor Samsat setempat</li>
						<li>Menyerahkan dokumen kendaraan
						<ul>
						<li>Asli BPKB atas nama peminjam/istri/suami/penjamin</li>
						<li>Asli Faktur</li>
						<li>Copy STNK yang masih berlaku Kwitansi kosong 3 lembar (lembar yang pertama bermaterai)</li>
						<li>Copy KTP pemilik pertama sepcrti yang tertera di BPKB dan kwitansi jual beli dari pemilik sebelumnya ke calon peminjam</li>
						</ul>
						</li>
						</ul>
